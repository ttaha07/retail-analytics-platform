USE DATABASE RETAIL_ANALYTICS_DB;

SELECT
    'BRONZE_CUSTOMERS' AS TABLE_NAME,
    'BRONZE' AS LAYER,
    COUNT(*) AS ROW_COUNT
FROM BRONZE.BRONZE_CUSTOMERS

UNION ALL

SELECT
    'BRONZE_ORDERS',
    'BRONZE',
    COUNT(*)
FROM BRONZE.BRONZE_ORDERS

UNION ALL

SELECT
    'BRONZE_PRODUCTS',
    'BRONZE',
    COUNT(*)
FROM BRONZE.BRONZE_PRODUCTS

UNION ALL

SELECT
    'BRONZE_ORDER_ITEMS',
    'BRONZE',
    COUNT(*)
FROM BRONZE.BRONZE_ORDER_ITEMS

UNION ALL

