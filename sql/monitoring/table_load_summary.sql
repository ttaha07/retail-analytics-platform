    'SILVER_ORDERS',
    'SILVER',
    COUNT(*)
FROM SILVER.SILVER_ORDERS

UNION ALL

SELECT
    'SILVER_PRODUCTS',
    'SILVER',
    COUNT(*)
FROM SILVER.SILVER_PRODUCTS

