USE DATABASE RETAIL_ANALYTICS_DB;
USE SCHEMA GOLD;

-- Customer retention by first purchase cohort
-- FACT_SALES is item-level, so we deduplicate to order/customer grain first.
WITH customer_orders AS (
    SELECT DISTINCT
        CUSTOMER_UNIQUE_ID,
        ORDER_ID,
        DATE_TRUNC('month', ORDER_PURCHASE_TIMESTAMP) AS ORDER_MONTH
    FROM FACT_SALES
    WHERE CUSTOMER_UNIQUE_ID IS NOT NULL
      AND ORDER_PURCHASE_TIMESTAMP IS NOT NULL
),

customer_cohorts AS (
    SELECT
        CUSTOMER_UNIQUE_ID,
        MIN(ORDER_MONTH) AS COHORT_MONTH
    FROM customer_orders
    GROUP BY CUSTOMER_UNIQUE_ID
),

cohort_activity AS (
    SELECT
        cc.COHORT_MONTH,
        co.ORDER_MONTH,
        DATEDIFF('month', cc.COHORT_MONTH, co.ORDER_MONTH) AS MONTHS_SINCE_FIRST_ORDER,
        co.CUSTOMER_UNIQUE_ID
    FROM customer_orders co
    INNER JOIN customer_cohorts cc
        ON co.CUSTOMER_UNIQUE_ID = cc.CUSTOMER_UNIQUE_ID
),

retention_counts AS (
    SELECT
        COHORT_MONTH,
        MONTHS_SINCE_FIRST_ORDER,
        COUNT(DISTINCT CUSTOMER_UNIQUE_ID) AS RETAINED_CUSTOMERS
    FROM cohort_activity
    GROUP BY
        COHORT_MONTH,
        MONTHS_SINCE_FIRST_ORDER
)

SELECT
    COHORT_MONTH,
    MONTHS_SINCE_FIRST_ORDER,
    RETAINED_CUSTOMERS,
    ROUND(
        RETAINED_CUSTOMERS * 100.0
        / FIRST_VALUE(RETAINED_CUSTOMERS) OVER (
            PARTITION BY COHORT_MONTH
            ORDER BY MONTHS_SINCE_FIRST_ORDER
        ),
        2
    ) AS RETENTION_RATE_PCT
FROM retention_counts
ORDER BY
    COHORT_MONTH,
    MONTHS_SINCE_FIRST_ORDER;